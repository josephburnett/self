package db

import "math/rand"

func NewId() Id {
	return Id(randSeq(32))
}

// https://stackoverflow.com/questions/22892120

var letters = []rune("abcdefghijklmnopqrstuvwxyzABCDEFGHIJKLMNOPQRSTUVWXYZ")

func randSeq(n int) string {
	b := make([]rune, n)
	for i := range b {
		b[i] = letters[rand.Intn(len(letters))]
	}
	return string(b)
}
